async def test_fs_ai_013_ac7_model_drift_metrics():
    """FS-AI-013 AC-7: detect_model_drift identifies drift when patterns change."""
    svc = _make_service()
    now = datetime.now(timezone.utc)

    # Historical baseline: stable latency ~500ms, tokens ~300
    for i in range(20):
        svc.record_llm_call(
            provider="openai", model="gpt-4o",
            prompt_tokens=200, completion_tokens=100,
            latency_ms=500,
            timestamp=now - timedelta(hours=48, seconds=-i),
        )

    # Recent window: significantly higher latency ~2000ms, tokens ~800
    for i in range(10):
        svc.record_llm_call(
            provider="openai", model="gpt-4o",
            prompt_tokens=500, completion_tokens=300,
            latency_ms=2000,
            timestamp=now - timedelta(hours=6, seconds=-i),
        )

    result = svc.detect_model_drift(window_hours=24)

    assert isinstance(result, ModelDriftResult)
    assert result.drift_score > 0.3  # Should detect drift
    assert result.is_drifting is True
    assert len(result.details) > 0


