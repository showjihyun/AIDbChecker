def _make_service() -> LLMObservabilityService:
    """Create a fresh (non-singleton) service for isolated tests."""
    return LLMObservabilityService()


def _seed_records(
    svc: LLMObservabilityService,
    count: int = 5,
    *,
    model: str = "gpt-4o",
    provider: str = "openai",
    latency_ms: int = 1200,
    prompt_tokens: int = 500,
    completion_tokens: int = 200,
    has_hallucination: bool = False,
    is_correct: bool | None = None,
    base_time: datetime | None = None,
) -> list[LLMCallRecord]:
    """Insert *count* records and return them."""
    base = base_time or datetime.now(timezone.utc)
    records = []
    for i in range(count):
        r = svc.record_llm_call(
            provider=provider,
            model=model,
            prompt_tokens=prompt_tokens,
            completion_tokens=completion_tokens,
            latency_ms=latency_ms,
            has_hallucination=has_hallucination,
            is_correct=is_correct,
            timestamp=base + timedelta(seconds=i),
        )
        records.append(r)
    return records


